package LoopingStatement;

public class Forpattern4 {

	public static void main(String[] args) {
		
		
		for(int i=0; i<=10; i++)
		{
			for(int j=0; j<=9-i; j++)
			{
				System.out.print(" ");
			}
			for(int k=0; k<=i; k++)
			{
				System.out.print("* ");
			}
			System.out.println();
		}
		for(int a=0; a<=5; a++)
		{
			for(int b=0; b<10-2; b++)
			{
				System.out.print(" ");
			}
			for(int c=0; c<=2; c++)
			{
				System.out.print("* ");
			}
			 System.out.println();
		}
		
	{
		
	}
	        
	}
}
		
	

